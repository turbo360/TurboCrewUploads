import { useState, useEffect, useRef } from 'react';
import { useAuthStore } from '../stores/authStore';
import { useSessionStore } from '../stores/sessionStore';

export default function StartPage() {
  const { login, isAuthenticated, userName, userEmail, isLoading: authLoading, error: authError, clearError: clearAuthError } = useAuthStore();
  const { createSession, isLoading: sessionLoading, error: sessionError, clearError: clearSessionError } = useSessionStore();

  const [name, setName] = useState('');
  const [email, setEmail] = useState('');
  const [projectName, setProjectName] = useState('');

  const projectRef = useRef<HTMLInputElement>(null);
  const nameRef = useRef<HTMLInputElement>(null);

  const isMac = window.electronAPI?.platform === 'darwin';

  // Pre-fill name + email if returning user (authenticated but no session)
  useEffect(() => {
    if (isAuthenticated && userName) {
      setName(userName);
    }
    if (isAuthenticated && userEmail) {
      setEmail(userEmail);
    }
  }, [isAuthenticated, userName, userEmail]);

  // Auto-focus: project field if returning user, name field if new
  useEffect(() => {
    if (isAuthenticated && userName) {
      projectRef.current?.focus();
    } else {
      nameRef.current?.focus();
    }
  }, [isAuthenticated, userName]);

  const isLoading = authLoading || sessionLoading;
  const error = authError || sessionError;

  const clearError = () => {
    clearAuthError();
    clearSessionError();
  };

  const isValid = name.trim().length > 0 && email.trim().length > 0 && projectName.trim().length > 0;

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!isValid || isLoading) return;

    // Step 1: Login (get JWT) if not already authenticated
    if (!isAuthenticated) {
      const loginSuccess = await login(name.trim(), email.trim());
      if (!loginSuccess) return;
    }

    // Step 2: Create session (projectName, crewName)
    await createSession(projectName.trim(), name.trim());
  };

  return (
    <div className={`min-h-screen flex items-center justify-center px-4 ${isMac ? 'pt-8' : ''}`}>
      <div className="w-full max-w-md">
        <div className="bg-gray-900/90 backdrop-blur-sm rounded-2xl shadow-2xl p-8 border border-gray-700/50">
          {/* Logo */}
          <div className="text-center mb-8">
            <img
              src="./logo-dark.png"
              alt="Turbo 360"
              className="h-16 mx-auto mb-4"
              onError={(e) => {
                e.currentTarget.style.display = 'none';
              }}
            />
            <h1 className="text-2xl font-bold text-white">Crew Upload</h1>
            <p className="text-gray-400 mt-2">Enter your details to start uploading</p>
          </div>

          {/* Error message */}
          {error && (
            <div className="mb-6 bg-red-900/50 border border-red-500 text-red-200 px-4 py-3 rounded-lg">
              <p>{error}</p>
              <button
                type="button"
                onClick={clearError}
                className="text-sm underline mt-1"
              >
                Dismiss
              </button>
            </div>
          )}

          {/* Start form */}
          <form onSubmit={handleSubmit} className="space-y-5">
            {/* Name */}
            <div>
              <label htmlFor="name" className="block text-sm font-medium text-gray-300 mb-2">
                Your Name <span className="text-red-400">*</span>
              </label>
              <input
                ref={nameRef}
                id="name"
                type="text"
                value={name}
                onChange={(e) => setName(e.target.value)}
                className="w-full px-4 py-3 bg-gray-800/80 border border-gray-600 rounded-lg text-white placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-orange-500 focus:border-transparent"
                placeholder="e.g., John Smith"
                required
                disabled={isLoading}
              />
            </div>

            {/* Email */}
            <div>
              <label htmlFor="email" className="block text-sm font-medium text-gray-300 mb-2">
                Email Address <span className="text-red-400">*</span>
              </label>
              <input
                id="email"
                type="email"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                className="w-full px-4 py-3 bg-gray-800/80 border border-gray-600 rounded-lg text-white placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-orange-500 focus:border-transparent"
                placeholder="e.g., john@example.com"
                required
                disabled={isLoading}
              />
            </div>

            {/* Project Name */}
            <div>
              <label htmlFor="projectName" className="block text-sm font-medium text-gray-300 mb-2">
                Project Name <span className="text-red-400">*</span>
              </label>
              <input
                ref={projectRef}
                id="projectName"
                type="text"
                value={projectName}
                onChange={(e) => setProjectName(e.target.value)}
                className="w-full px-4 py-3 bg-gray-800/80 border border-gray-600 rounded-lg text-white placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-orange-500 focus:border-transparent"
                placeholder="e.g., Toyota Campaign 2024"
                required
                disabled={isLoading}
              />
            </div>

            <button
              type="submit"
              disabled={isLoading || !isValid}
              className="w-full py-3 px-4 bg-orange-500 hover:bg-orange-600 disabled:bg-gray-600 disabled:cursor-not-allowed text-white font-medium rounded-lg transition-colors flex items-center justify-center gap-2"
            >
              {isLoading ? (
                <span className="flex items-center justify-center gap-2">
                  <svg className="animate-spin h-5 w-5" viewBox="0 0 24 24">
                    <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4" fill="none" />
                    <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z" />
                  </svg>
                  Starting...
                </span>
              ) : (
                <>
                  Start Uploading
                  <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M14 5l7 7m0 0l-7 7m7-7H3" />
                  </svg>
                </>
              )}
            </button>
          </form>
        </div>
      </div>
    </div>
  );
}
